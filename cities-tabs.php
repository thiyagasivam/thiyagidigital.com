<script>
document.addEventListener('DOMContentLoaded', function() {
    const mobileSelect = document.getElementById('cityMobileSelect');
    
    if (mobileSelect) {
        mobileSelect.addEventListener('change', function() {
            const selectedCity = this.value;
            const targetTab = document.getElementById(selectedCity + '-tab');
            
            if (targetTab) {
                // Trigger the tab
                const tab = new bootstrap.Tab(targetTab);
                tab.show();
            }
        });
    }
    
    // Update mobile dropdown when desktop tab is clicked
    const tabButtons = document.querySelectorAll('[data-bs-toggle="pill"]');
    tabButtons.forEach(function(button) {
        button.addEventListener('shown.bs.tab', function() {
            const cityId = this.getAttribute('data-bs-target').replace('#', '');
            if (mobileSelect) {
                mobileSelect.value = cityId;
            }
        });
    });
    
    // Add click tracking for service links
    const serviceLinks = document.querySelectorAll('.service-link');
    serviceLinks.forEach(function(link) {
        link.addEventListener('click', function(e) {
